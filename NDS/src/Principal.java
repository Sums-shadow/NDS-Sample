        public static Connection connecterBD() throws SQLException {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            System.out.println("Driver");
            String url="jdbc:mysql://localhost:3306/base";
            String user="root";
            String password="";
            String strQuery = "SELECT user.* FROM `user` INNER JOIN session ON user.idUser=session.idUser WHERE session.idSession=( SELECT MAX(session.idSession) FROM session)";
            
            
            
            Connection con =DriverManager.getConnection(url,user,password);
            Statement stUsers = con.createStatement(); 
            
            ResultSet rsUsers = stUsers.executeQuery(strQuery); 
              while(rsUsers.next()) { 
                    Utilisateur util=new Utilisateur();
                    util.setIdent(rsUsers.getInt(1));
                    util.setNomUtilisateur(rsUsers.getString(2));
                    util.setPoste(rsUsers.getString(4));
                    util.setMotdePass(rsUsers.getString(3));
                    util.setNvA(rsUsers.getInt(5));
                    idUtil=util.getIdent();
                    nvA=util.getNvA();
                    poste=util.getPoste();
                    nomUtil=util.getNomUtilisateur();
                    System.out.println(idUtil+" "+nomUtil+" "+poste+" "+nvA);
                    
                   
                     }
              rsUsers.close(); 
              return con;
              
              } catch (Exception e){
            e.printStackTrace();
            return null;
               }
              
    }

